package com.liferay.portalweb.portlet.documentsandmedia.portlet.configuredmportletshowdocumentcolumns;

import com.liferay.portalweb.portal.BaseTestCase;
import com.liferay.portalweb.portal.util.RuntimeVariables;

/**
 * @author Brian Wing Shun Chan
 */
public class ConfigureDMPortletShowDocumentColumnsTest extends BaseTestCase {
	public void testConfigureDMPortletShowDocumentColumns()
		throws Exception {
		selenium.selectWindow("null");
		selenium.selectFrame("relative=top");
		selenium.open("/web/guest/home/");
		selenium.clickAt("link=Documents and Media Test Page",
			RuntimeVariables.replace("Documents and Media Test Page"));
		selenium.waitForPageToLoad("30000");
		Thread.sleep(5000);
		assertEquals(RuntimeVariables.replace("Options"),
			selenium.getText("//span[@title='Options']/ul/li/strong/a"));
		selenium.clickAt("//span[@title='Options']/ul/li/strong/a",
			RuntimeVariables.replace("Options"));
		selenium.waitForVisible(
			"//div[@class='lfr-component lfr-menu-list']/ul/li[2]/a");
		assertEquals(RuntimeVariables.replace("Configuration"),
			selenium.getText(
				"//div[@class='lfr-component lfr-menu-list']/ul/li[2]/a"));
		selenium.clickAt("//div[@class='lfr-component lfr-menu-list']/ul/li[2]/a",
			RuntimeVariables.replace("Configuration"));
		selenium.waitForElementPresent(
			"//iframe[contains(@id,'configurationIframeDialog')]");
		selenium.selectFrame(
			"//iframe[contains(@id,'configurationIframeDialog')]");
		Thread.sleep(5000);
		selenium.waitForVisible("//select[@id='_86_availableEntryColumns']");
		selenium.addSelection("//select[@id='_86_availableEntryColumns']",
			RuntimeVariables.replace("Name"));
		selenium.clickAt("xPath=(//button[@title='Move selected items from Available to Current.'])[2]",
			RuntimeVariables.replace(
				"Move selected items from Available to Current"));
		selenium.waitForPartialText("//select[@id='_86_currentEntryColumns']",
			"Name");
		selenium.clickAt("xPath=(//button[@title='Move selected item in Current one position down.'])[2]",
			RuntimeVariables.replace(
				"Move selected item in Current one position down."));
		selenium.waitForPartialText("//select[@id='_86_currentEntryColumns']",
			"Name");
		selenium.clickAt("//input[@value='Save']",
			RuntimeVariables.replace("Save"));
		selenium.waitForPageToLoad("30000");
		assertEquals(RuntimeVariables.replace(
				"You have successfully updated the setup."),
			selenium.getText("//div[@class='portlet-msg-success']"));
		selenium.selectFrame("relative=top");
	}
}