package com.liferay.exportimport.messaging;

import com.liferay.portal.kernel.messaging.DestinationNames;
import com.liferay.portal.kernel.messaging.MessageListener;
import com.liferay.portal.kernel.module.framework.ModuleServiceLifecycle;
import com.liferay.portal.kernel.scheduler.messaging.SchedulerEventMessageListenerWrapper;

import org.osgi.service.component.annotations.Activate;
import org.osgi.service.component.annotations.Component;
import org.osgi.service.component.annotations.Reference;

/**
 * @author Daniel Kocsis
 */
@Component(
	immediate = true,
	property = {"destination.name=" + DestinationNames.LAYOUTS_LOCAL_PUBLISHER},
	service = MessageListener.class
)
public class LayoutsLocalPublisherMessageListenerWrapper
	extends SchedulerEventMessageListenerWrapper {

	@Activate
	protected void activate() {
		setMessageListener(new LayoutsLocalPublisherMessageListener());
	}

	@Reference(target = ModuleServiceLifecycle.PORTAL_INITIALIZED, unbind = "-")
	protected void setModuleServiceLifecycle(
		ModuleServiceLifecycle moduleServiceLifecycle) {
	}

}