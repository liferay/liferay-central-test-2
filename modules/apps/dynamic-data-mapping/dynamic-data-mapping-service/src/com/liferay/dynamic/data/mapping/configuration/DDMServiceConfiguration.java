package com.liferay.dynamic.data.mapping.configuration;

import aQute.bnd.annotation.metatype.Meta;

/**
 * @author Lino Alves
 */
@Meta.OCD(
	id = "com.liferay.dynamic.data.mapping.configuration.DDMServiceConfiguration",
	name = "Dynamic Data Mapping Service Configuration"
)
public interface DDMServiceConfiguration {

	/**
	 * Set this to true if dynamic data mapping structure keys should always be
	 * autogenerated.
	 */
	@Meta.AD(
		deflt = "true",
		name ="Automatically generate dynamic data mapping structure keys",
		required = false
	)
	public boolean autogenerateStructureKey();

	/**
	 * Set this to true if dynamic data mapping template keys should always be
	 * autogenerated.
	 */
	@Meta.AD(
		deflt = "true",
		name ="Automatically generate for dynamic data mapping template keys",
		required = false
	)
	public boolean autogenerateTemplateKey();

	/**
	 * Set the name of template language which will be used by default.
	 */
	@Meta.AD(
		deflt = "ftl", name ="Default template language",
		optionLabels = {
			"FreeMarker", "Velocity", "Extensible Stylesheet Language"
		},
		optionValues = {"ftl", "vm", "xls"}, required = false
	)
	public String defaultTemplateLanguage();

}