package com.liferay.dynamic.data.mapping.configuration;

import aQute.bnd.annotation.metatype.Meta;

/**
 * @author Lino Alves
 */
@Meta.OCD(
		id = "com.liferay.dynamic.data.mapping.configuration.DDMConfiguration",
		name = "Dynamic Data Mapping Configuration"
	)
public interface DDMConfiguration {

	/**
	 *  Set this to true if dynamic data mapping structure keys should always be
	 *  autogenerated.
	 */
	@Meta.AD(
		deflt = "true",
		name ="Key auto generation for dynamic data mapping structures",
		required = false
	)
	public boolean ddmStructureForceAutogenerateKey();

	/**
	 *  Set this to true if dynamic data mapping template keys should always be
	 *	autogenerated.
	 */
	@Meta.AD(
		deflt = "true",
		name ="Key auto generation for dynamic data mapping templates",
		required = false)
	public boolean ddmTemplateForceAutogenerateKey();

	/**
	 * Set the name of template language which will be used by default.
	 *
	 */
	@Meta.AD(
			deflt = "ftl", name ="Default template language",
			optionLabels = {"ftl", "vm", "xls"},
			optionValues = {"ftl", "vm", "xls"}, required = false
	)
	public String ddmTemplateLanguageDefault();

}